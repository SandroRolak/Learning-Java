package javanapratica;

public class ExemploIfElse2 {

	public static void main(String[] args) {
		
		int num = 9;
		
		if (num>10) {
			System.out.println(" Maior que 10");
		} else {
			if (num<10) {
				System.out.println("Menor que 10");
			} else {
				System.out.println("Igual a 10");
			}
		}

	}

}
