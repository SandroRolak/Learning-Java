package javanapratica;

public class CastingPrimitivos {

	public static void main(String[] args) {

		int x = 10;
		double y = x;
		long z = 10L;
		
		float w = 10.0F;
		int r = (int)14.5;
		
		byte b = 127;
		
		System.out.println(x + " , " + y + " , " + z + " , " + w  + " , " + r + " , " + b);
	}

}
